int		sum(int val1, int val2)
{
	return (val1 + val2);
}

int		sub(int val1, int val2)
{
	return (val1 - val2);
}

int		mul(int val1, int val2)
{
	return (val1 * val2);
}

int		div(int val1, int val2)
{
	return (val1 / val2);
}

int		mod(int val1, int val2)
{
	return (val1 % val2);
}
